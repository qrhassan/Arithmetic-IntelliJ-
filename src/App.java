public class App {
    public static void main(String[] args) {
        System.out.println(10+5);
        System.out.println(9/3); //Divide

        //Surprise
        System.out.println(10/3); //Divide

        System.out.println(10.0/3.0); //Divide
        System.out.println(9/4.0); //one floating point value gives floating point results

        System.out.println(2*2); //Multiply

        System.out.println(8-2); //Minus
    }
}
